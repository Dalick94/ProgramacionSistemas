#6. LA FUNCIÓN EMPTY() CREA UNA MATRIZ VACÍA CON LAS DIMENSIONES QUE LE DIGAMOS
#A OJOS DEL USUARIO PUEDE SER LO MISMO QUE ZEROS(), PERO SI OPERAMOS CON EL SISTEMA Y LA HEMOS CREADO
#COMO EMPTY, DARÍA ERRORES AL NO DETECTAR VALORES E INTENTAR REAIZAR OPERACIONES
