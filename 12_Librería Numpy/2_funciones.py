#8. LA FUNCIÓN LINSPACE() CREA UNA MATRIZ 1D ENTRE UN VALOR Y OTRO DEL TAMAÑO QUE LE DIGAMOS.
#NO ES LO MISMO QUE LA ARANGE() DE TRES PARÁMETROS QUE CUANDO LLEGA AL LÍMITE DEJA DE CREAR VALORES.
