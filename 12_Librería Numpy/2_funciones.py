#3. LA FUNCIÓN ARANGE() GENERA DE FORMA ALEATORIA ARRAYS 1D DE VALORES Y DIMENSIONES QUE LE DIGAMOS.
    #LA FUNCIÓN BÁSICA ADMITE DOS PARÁMETROS. EL PRIMERO ES >=VALOR 1 Y EL SEGUNDO CUMPLE <.
a = np.arange(0,90)
print(a)
print()

    #LA FUNCIÓN ARANGE() AVANZADA ADMITE TRES PARÁMETROS, VALOR INICIAL, VALOR FINAL Y PASO ENTRE VALORES.
