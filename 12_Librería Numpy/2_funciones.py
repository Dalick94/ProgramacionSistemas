print()

#4. LA FUNCIÓN ZERO() O ONES() GENERA ARRAYS CON CONTENIDO 0 Y LAS DIMENSIONES QUE LE DIGAMOS.
    #LA FUNCIÓN BÁSICA CREA VECTORES O ARRAYS 1D
c = np.zeros(4)
print(c)
d = np.ones(4)
print(d)
e = np.ones(4)
print(e)
print()

    #LA FUNCIÓN BÁSICA CREA MATRICES O ARRAYS 2D
f = np.zeros((3,4))
print(f)
g = np.ones((3,4))
print(g)
h = np.ones((3,4))
print(h)
print()

    #LAS FUNCIONES AVANZADAS PERMITEN REALIZAR OPERACIONES ENTRE ARRAYS.
i = np.array(c + d + e)
print(i)
print()
j = np.array(f + g + h)
print(j)
print()

